/**
 * Detects the subject based on the document's name and uploaded custom text.
 */
function detectSubject(title: string, text: string): 'history' | 'science' | 'tech' | 'arabic' | 'general' {
  const combined = (title + " " + text).toLowerCase();
  
  if (
    combined.includes("تاريخ") || 
    combined.includes("history") || 
    combined.includes("حضارة") || 
    combined.includes("أندلس") || 
    combined.includes("ثقافة") || 
    combined.includes("شخصيات") || 
    combined.includes("سيرة")
  ) {
    return 'history';
  }
  
  if (
    combined.includes("فيزياء") || 
    combined.includes("physics") || 
    combined.includes("كيمياء") || 
    combined.includes("chemistry") || 
    combined.includes("علوم") || 
    combined.includes("science") || 
    combined.includes("رياضيات") || 
    combined.includes("math") || 
    combined.includes("أحياء") || 
    combined.includes("biology")
  ) {
    return 'science';
  }
  
  if (
    combined.includes("برمجة") || 
    combined.includes("programming") || 
    combined.includes("حاسوب") || 
    combined.includes("computer") || 
    combined.includes("ذكاء") || 
    combined.includes("تقنية") || 
    combined.includes("تكنولوجيا") || 
    combined.includes("كود") || 
    combined.includes("ويب") || 
    combined.includes("برمج")
  ) {
    return 'tech';
  }

  if (
    combined.includes("نحو") || 
    combined.includes("عربي") || 
    combined.includes("لغة") || 
    combined.includes("صرف") || 
    combined.includes("بلاغة") || 
    combined.includes("أدب") || 
    combined.includes("قصيدة")
  ) {
    return 'arabic';
  }
  
  return 'general';
}

/**
 * Normalizes the name of the textbook for beautiful display in the UI.
 */
export function cleanBookTitle(fileName: string): string {
  let name = fileName.replace(/\.[^/.]+$/, ""); // strip extension
  name = name.replace(/[_-]/g, " "); // replace underscores/dashes with spaces
  
  // Translation for common standard placeholders
  if (name.toLowerCase() === "ibn khaldun" || name === "ibn_khaldun") {
    return "مقدمة ابن خلدون ونظريات الاجتماع";
  }
  if (!name.trim()) {
    return "كتاب مدرسي عام";
  }
  return name;
}

/**
 * Main curriculum generator. Spits out 5 custom levels and exactly 50 localized interactive questions.
 */
export function generateDynamicBookNode(fileName: string, customText: string): { node: SkillNode, plans: StudyPlan[] } {
  const cleanTitle = cleanBookTitle(fileName);
  const subject = detectSubject(cleanTitle, customText);
  
  const nodeId = "custom_textbook_node";
  
  // 1. Generate study plans based on the subject
  const plans = getStudyPlansForSubject(subject, cleanTitle);
  
  // 2. Generate lessons and their corresponding questions
  const lessons: Lesson[] = [];
  
  for (let i = 1; i <= 5; i++) {
    lessons.push({
      id: `textbook_level_${i}`,
      title: plans[i - 1].title,
      xpReward: 30 + (i * 5),
      questions: getQuestionsForSubjectAndLevel(subject, i, cleanTitle)
    });
  }
  
  const node: SkillNode = {
    id: nodeId,
    title: `كتاب: ${cleanTitle}`,
    icon: subject === 'science' ? 'Atom' : subject === 'tech' ? 'Brain' : subject === 'arabic' ? 'Lightbulb' : 'Award',
    description: `فصل مخصص من كتابك المدرسي (${cleanTitle}). استكشف واختبر فہمك عبر 5 مستويات تدرجية بذكاء سقراطي.`,
    levelCount: 5,
    requiredNodes: [],
    lessons
  };
  
  return { node, plans };
}

