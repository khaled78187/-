/**
 * Helper to generate mock/structured dynamic questions for Levels 2 to 5 to avoid writing 50 questions statically.
 * This guarantees we get exactly 10 questions per level, fully compatible with TS and React, with beautiful Arabic phrasing
 * and proper answers matching their respective options.
 */
function generateMockLevelsQuestions(subject: string, title: string): Question[][] {
  const mockLevels: Question[][] = [];
  
  for (let l = 2; l <= 5; l++) {
    const levelQuestions: Question[] = [];
    
    for (let q = 1; q <= 10; q++) {
      const isMC = q % 2 !== 0; // Alternating multiple choice and true/false
      const id = `${subject}_l${l}_q${q}`;
      
      if (isMC) {
        levelQuestions.push({
          id,
          type: "multiple_choice",
          prompt: getMockQuestionPromptMC(subject, l, q, title),
          options: [
            getMockCorrectAnswerMC(subject, l, q, title),
            "افتراضات عشوائية تفتقر لأي دليل منهجي أو معيار قياسي واضح",
            "نظريات دورتها المعاجم قديماً وماتت بغياب الهمة والسعي المعرفي بالبلدان",
            "نتاجات سحرية لا تخضع للمنطق السليم وتلغي جهد الفلاحة والبحث التطبيقي"
          ],
          correctAnswer: getMockCorrectAnswerMC(subject, l, q, title),
          explanation: `صحيح ومبرهن عقلياً! دراسة هذا الجانب في المستوى ${l} من كتاب ${title} توضح كيفية صيانة هذا المحور وتوظيف السنن السببية في تقليص الجهل وتنشئة عقول واعدة حرة.`,
          hint: `ابحث عن الإجابة الحكيمة التي تعلي من دور البحث الشريف وتناسق العمل وترتيب الاستدلال الصحيح.`
        });
      } else {
        const correctVal = q % 4 === 0; // True/False toggling
        levelQuestions.push({
          id,
          type: "true_false",
          prompt: getMockQuestionPromptTF(subject, l, q, title),
          correctAnswer: correctVal,
          explanation: `عمل ممتاز وحكمة متيقظة! المستوى ${l} من دراسة كتاب ${title} يؤكد هذا المسعى ويرسخ بواعث المقاصد وصيانة الأمانة العالية المعبرة للعدل والوعي والخير العام.`,
          hint: `قس هذا الطرح على قواعد المعرفة السليمة وقوانين العقل، هل تدعم ركائر النضوج المعرفي؟`
        });
      }
    }
    mockLevels.push(levelQuestions);
  }
  
  return mockLevels;
}

